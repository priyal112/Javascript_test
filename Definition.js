
//What is string interpolation and concatenation. Write definition with example.


//String interpolation:- String interpolation in JavaScript is a process in which an expression is inserted
//                       or placed in the string. To insert or embed this expression into the string a template
//                       literal is used.
// Example-
const age = 22;
const message = `I am ${age} years old.`;

console.log(message); // Output: I am 22 years old.

//String Concatenation:- String Concatenation in JavaScript means appending
//                       one or more strings to the end of another string.
//Example-

const firstName = "Priyal";
const lastName = "Shrivastava";
const fullName = firstName + " " + lastName;

console.log(fullName); // Output: Priyal Shrivastava
